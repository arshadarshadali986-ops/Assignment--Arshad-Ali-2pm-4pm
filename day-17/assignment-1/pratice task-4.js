//This is a example to show the difference between == (Equals to) & === (Strict equals to)
const a = "69";

console.log(a == 69); //This gives a TRUE because it same in only the value although both are difference.
console.log(a === 69); //This gives a False because i give 69 as a String ( === ) this is called Strict equals which checks the data type of the value.