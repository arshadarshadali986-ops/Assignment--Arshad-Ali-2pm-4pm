//Student grading sysment

let mark = 69;

if ( mark <= 100 && mark >= 90) {
  console.log("A")
}
else if (mark <= 90 && mark >= 80) {
  console.log("B")
}
else if (mark <= 80 && mark >= 70) {
  console.log("C")
}
else if (mark <= 70 && mark >= 60) {
  console.log("D")
}
else if (mark <= 60 && mark >= 50) {
  console.log("E")
}
else if (mark <= 50 && mark >= 0) {
  console.log("F")
}