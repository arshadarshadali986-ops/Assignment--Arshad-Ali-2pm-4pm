let Day = 3;

switch (Day){

  case 1:
    console.log("Monday");
    break;

  case 2:
    console.log("Tuseday");
     break;

  case 3:
    console.log("Wednesday");
    break;

  case 4:
    console.log("Thrusday");
    break;

  case 5:
    console.log("Friday");
    break;

  case 6:
    console.log("Staruday");
    break;

  case 7:
    console.log("Sunday");
    break;

    default :
    console.log("Invaild Input");
}